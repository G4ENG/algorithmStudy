/*
 첫째 줄에 수의 개수 N(1 ≤ N ≤ 1,000)이 주어진다. 둘째 줄부터 N개의 줄에는 숫자가 주어진다. 이 수는 절댓값이 1,000보다 작거나 같은 정수이다. 수는 중복되지 않는다.
 */

let tc = Int(readLine()!)!
var arr = [Int]()
for _ in 0..<tc {
    arr.append(Int(readLine()!)!)
}



for num in arr {
    print(num)
}

